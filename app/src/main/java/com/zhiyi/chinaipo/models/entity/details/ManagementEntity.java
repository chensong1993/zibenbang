package com.zhiyi.chinaipo.models.entity.details;


public class ManagementEntity {
    private String changedate;
    private String begin_vol;
    private String end_vol;
    private String chng_vol;
    private String post;
    private String holder;
    private String mng_name;

    public String getChangedate() {
        return changedate;
    }

    public void setChangedate(String changedate) {
        this.changedate = changedate;
    }

    public String getBegin_vol() {
        return begin_vol;
    }

    public void setBegin_vol(String begin_vol) {
        this.begin_vol = begin_vol;
    }

    public String getEnd_vol() {
        return end_vol;
    }

    public void setEnd_vol(String end_vol) {
        this.end_vol = end_vol;
    }

    public String getChng_vol() {
        return chng_vol;
    }

    public void setChng_vol(String chng_vol) {
        this.chng_vol = chng_vol;
    }

    public String getPost() {
        return post;
    }

    public void setPost(String post) {
        this.post = post;
    }

    public String getHolder() {
        return holder;
    }

    public void setHolder(String holder) {
        this.holder = holder;
    }

    public String getMng_name() {
        return mng_name;
    }

    public void setMng_name(String mng_name) {
        this.mng_name = mng_name;
    }
}
